package com.zzy.tool.util;

public class TranslateBean{
	private String srcLANG;  //原字符串
	private String targetLANG;    //目标字符串
	private String keyWord;  //关键词
	private String srcLine; 
	private String targetLine;
	public String getSrcLANG() {
		return srcLANG;
	}
	public void setSrcLANG(String srcLANG) {
		this.srcLANG = srcLANG;
	}
	public String getTargetLANG() {
		return targetLANG;
	}
	public void setTargetLANG(String targetLANG) {
		this.targetLANG = targetLANG;
	}
	public String getKeyWord() {
		return keyWord;
	}
	public void setKeyWord(String keyWord) {
		this.keyWord = keyWord;
	}
	public String getSrcLine() {
		return srcLine;
	}
	public void setSrcLine(String srcLine) {
		this.srcLine = srcLine;
	}
	public String getTargetLine() {
		return targetLine;
	}
	public void setTargetLine(String targetLine) {
		this.targetLine = targetLine;
	} 

		
}
